package assignment;

public class Ass_2_DeclareAndInitializeDataTypeAndVariable {

	public static void main(String[] args) {
		
		byte a =100; //-128 to 127 stores small numbers -Its also called declaration and initialization
		//Declaring data type 'byte' data type 
		//Initializing it with value 100

		System.out.println(a); // Utilization- utilizing value 
		
		short b=2345;// -32768 to 32767 --declaration and initialization
		System.out.println(b);
		
		int c=8765433; // store bigger num --declaration and initialization
		System.out.println(c);
		
		long d=83575868567l; // more bigger bigger num stored-always right small L (l) at the end for long data type-declaration and initialization
		System.out.println(d);
		
		double e=9857.123897645745385847593578937788888888; // more precision used double , upto 19 digit long value after decimal-declaration and initialization
		System.out.println(e);
		
		float f=5245.45f; // can store 5-6 value after decimal - sufix 'f' i mandatory at the end-declaration and initialization
		System.out.println(f);
		
		char initial='X'; // store alphabet in single quote--declaration and initialization
		System.out.println(initial);
		
		String name="Tanuja"; // store anything in double quote	--declaration and initialization
		System.out.println(name);
		
		boolean answer=true; // true = 1, false = 0 -- declaration and initialization
		System.out.println(answer); // utilization
		
		int z; // declaration
		z=108; // initialization
		System.out.println(z); // utilization

	}

}
